    private void btnEmpGoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmpGoActionPerformed
         try{
            String sql="SELECT name,password FROM employee WHERE type='admin'";
            st = cn.prepareStatement(sql);
            rs=st.executeQuery();
            String adminName=null;
            String adminPasswordFromDb=null;
            
            while(rs.next())
            {
                adminPasswordFromDb=rs.getString("password");
                adminName=rs.getString("name");
            }
            
            if(txtAdminPassword.getText().equals(adminPasswordFromDb))
            {
                 btnBackHome.show();
                jTabbedPane1.setSelectedIndex(1);
                txtAdminPassword.setText(null);
                //hide admin checking panel
                jPanel6.hide();
                 //welcome note
                lblDisplayName.setText(adminName);
               
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Incorrect password");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Not Working the Search Query");
        }
        
    }//GEN-LAST:event_btnEmpGoActionPerformed
